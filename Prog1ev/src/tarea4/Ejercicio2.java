package tarea4;

public class Ejercicio2 {
	/**
	 * Escribe un programa Java que te muestre los números enteros entre 50 y 100 (ambos incluídos)
	 */
	
	public static void main(String[] args) {
		/**
		 * Pre:---
		 * Post:
		 */
			
		numeros();	
	}
	
	public static void numeros() {
		/**
		 * Pre:---
		 * Post: 
		 */
		
		for(int i = 50; i <= 100; i++) {
			System.out.print(i + " ");
		}
	}
}
